    This is a emulator of the <i>Sortes Alearum</i> (<i>sortes</i>, Latin for ‘lots’, and <i>alearum</i> from Latin meaning
    ‘dice’), an ancient lot oracle.
  </p>

  <h2>Casting the Lots</h2>
  <p>
    Lot oracles were a method of divination in antiquity using an element of randomness to answer a specific query or
    concern by selecting from a list of pre-written responses. Some lot oracles had a set of predefined questions with
    randomly chosen answers, and others, like this one, had a list of general answers for any question. The randomness
    of the roll provides the space for the divine to enter the conversation. Ancient sources discuss various kinds of
    lot oracles; some are skeptical of their efficacy or feel that they represent mere chance, not the divine. Not
    necessarily the most popular or high-prestige form of divination, they were popular enough that we have many
    examples from antiquity. This tradition carried on into through the Middle Ages in a Christianized form, bearing
    many similarities to its older, pagan relatives, despite mixed reactions from Church figures.
  </p>

  <h2>Sortes Alearum</h2>
  <p>
    The <i>Sortes Alearum</i> specifically is a set of texts found as epigrams carved on stone pillars at sites along
    southwestern Anatolia, in modern-day Turkey. On the pillars would have been fifty-six messages, each corresponding
    to a particular combination of die rolls. Five <i>astragaloi</i> (sheep’s knucklebones) would be rolled as dice.
    While more irregular and organic than cubic dice, each has four distinct sides, so there are fifty-six possible
    combinations. The four sides are labeled as one (called the <i>Chian</i>), three, four, and six (two and five are
    excluded), so that each set of opposite sides adds up to seven. The supplicant or interpreter then takes the sum of
    all numbers rolled and uses it to find the corresponding message, which were listed in order of sum. There is a
    message for each possible roll, and each one has words of advice and a divine figure associated with it, such as
    Zeus Olympios or Aphrodite. The text found on the main page is all directly from these translated epigrams.
  </p>

  <h2>Interpreting the Rolls</h2>
  <p>
    In “Rolling the Dice for an Answer” (the primary source for this project and the source of all the translated text),
    Fritz Graf writes that many of these oracle sites may have been in public spaces, such as by a town gate or agora.
    They may have been frequented by merchants and those who traveled for business, explaining the heavy focus on
    commerce in many of the fortunes. While oracles and divination were commonly the province of the god Apollo, whose
    most famous oracle was in Delphi, this one seems to have been linked with Hermes as well, who is associated with
    travel and commerce, as well as with chance.
  </p>

  <p>
    These fortunes seemed to have resonated with their specific audience in their time. Perhaps they may still ring true
    today then, as life in our highly-connected and mobile modern era is, compared to the largely agrarian context of
    antiquity, seemingly more dependent than ever on the kind of randomness and chance which comes from frequently
    moving around and relying on the whims and fortunes of strangers for financial success.
  </p>

